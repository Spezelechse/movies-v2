<h2>Willkommen im Adminbereich</h2>
<br />
Hier können Medien <b>hinzugefügt</b>, <b>gelöscht</b>, <b>bearbeitet</b>, <b>importiert</b>, <b>exportiert</b> und als <b>zurückgegeben markiert</b> werden.
Zusätzlich gibt es noch die Möglichkeit aus sämtlichen Medien eine <b>PDF-Liste</b> zu generieren.
<br /><br />
Außerdem können Benutzer <b>hinzugefügt</b>, <b>gelöscht</b> und <b>bearbeitet</b> werden.
<br /><br />
Für jeden Benutzer gibt es noch die Möglichkeit seine eigenen <b>Benutzerdaten</b> zu bearbeiten und der "Über"-Admin kann ein paar <b>Einstellungen für die Seite</b> tätigen.
<br /><br />
Es stehen allerdings nicht jedem Benutzer sämtliche <b>Verwaltungsoptionen</b> zur Verfügung, die muss einer der Admins für <b>jeden einzelnen Benutzer freischalten</b>.